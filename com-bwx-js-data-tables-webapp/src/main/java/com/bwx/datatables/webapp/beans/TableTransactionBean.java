package com.bwx.datatables.webapp.beans;

/**
 * Used to simulate a series of table transactions. The client can use the ID to
 * request a table stored server side. The description allows the client to
 * display details regarding what is being selected.
 * 
 * @author Chris Ludka
 *
 */
public class TableTransactionBean {

	private long id;

	private String description;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((description == null) ? 0 : description.hashCode());
		result = prime * result + (int) (id ^ (id >>> 32));
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		TableTransactionBean other = (TableTransactionBean) obj;
		if (description == null) {
			if (other.description != null)
				return false;
		} else if (!description.equals(other.description))
			return false;
		if (id != other.id)
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "TableTransactionBean [id=" + id + ", description=" + description + "]";
	}

}
